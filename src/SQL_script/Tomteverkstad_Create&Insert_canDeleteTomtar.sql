
drop database if exists Tomteverkstad;
create database Tomteverkstad;
use Tomteverkstad;

create table Tomte(
	Id int not null auto_increment primary key,
	namn varchar(100) not null unique
); 

create table Chefstomte(
	tomteId int not null primary key,
	foreign key (tomteId) references Tomte(Id) on delete cascade
);

create table Tomtehierarki(
	underordnad int not null,
	överordnad int not null,
	foreign key (underordnad) references Tomte(Id) on delete cascade,
	foreign key (överordnad) references Chefstomte(tomteId) on delete cascade
);

create table Tillverkningstomte(
	tomteId int not null primary key,
	foreign key (tomteId) references Tomte(Id) on delete cascade
);

create table Underrättelsetomte(
	tomteId int not null primary key,
	säkerhetsklass int not null,
	foreign key (tomteId) references Tomte(Id) on delete cascade
);

create table Land(
	id int not null auto_increment primary key,
	namn varchar(100) not null,
	ansvarig int,
	foreign key (ansvarig) references underrättelsetomte(tomteId) on delete set null
);

create table Present(
	id int not null auto_increment primary key,
	namn varchar(100) not null,
	tillverkningstomteId int,
	foreign key (tillverkningstomteId) references Tillverkningstomte(tomteId) on delete set null
);


create table Barn(
	id int not null auto_increment primary key,
	namn varchar(100) not null,
	adress varchar(100) not null,
	landId int not null,
	ansvarig int,
	foreign key (landId) references land(id),
	foreign key (ansvarig) references underrättelsetomte(tomteId) on delete set null
);

create table Önskning(
	id int not null auto_increment primary key,
	barnId int not null,
	presentId int not null,
	prioritetsordning int not null,
	foreign key (barnId) references Barn(Id),
	foreign key (presentId) references Present(Id)
);


create table Julklapp(
	id int not null auto_increment primary key,
	barnId int not null,
	presentId int not null,
	foreign key (barnId) references Barn(Id),
	foreign key (presentId) references Present(Id)
);


create table Rapport(
	id int not null auto_increment primary key,
	rapporttext varchar(300) not null,
	uppförandetal int not null,
	datum date not null,
	barnId int not null,
	skapare int,
	granskare int,
	foreign key (skapare) references underrättelsetomte(tomteId) on delete set null,
	foreign key (granskare) references underrättelsetomte(tomteId) on delete set null,
	foreign key (barnId) references Barn(Id)
);



